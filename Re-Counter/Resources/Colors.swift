import Foundation
import UIKit

//Theme palettes
extension UIColor{
    
    class var grayAlabaster: UIColor{
        return UIColor(red:0.83, green:0.84, blue:0.85, alpha:1.0)
    }
    
    class var darkCelurean: UIColor{
        return UIColor(red:0.05, green:0.29, blue:0.53, alpha:1.0)
    }
    
    class var warmGray: UIColor{
        return UIColor(red:0.96, green:0.96, blue:0.96, alpha:1.0)
    }
    
    class var pastelWhite: UIColor{
        return UIColor(red:0.98, green:0.98, blue:0.98, alpha:1.0)
    }
}

